function buildDataQualityWarnings(dataQuality, locale) {
  const warnings = [];
  if (dataQuality.blankDate) {
    warnings.push(
      locale === 'th'
        ? `พบ ${dataQuality.blankDate} รายการที่ไม่มีวันที่`
        : `${dataQuality.blankDate} records have missing dates.`
    );
  }
  if (dataQuality.futureDate) {
    warnings.push(
      locale === 'th'
        ? `พบ ${dataQuality.futureDate} รายการที่มีวันที่มีผลอยู่ในอนาคต`
        : `${dataQuality.futureDate} records have future effective dates.`
    );
  }
  if (dataQuality.missingReference) {
    warnings.push(
      locale === 'th'
        ? `พบ ${dataQuality.missingReference} รายการที่ไม่มีเลขอ้างอิง`
        : `${dataQuality.missingReference} records are missing reference numbers.`
    );
  }
  if (dataQuality.blankStatus) {
    warnings.push(
      locale === 'th'
        ? `พบ ${dataQuality.blankStatus} รายการที่ไม่มีสถานะการดำเนินการ`
        : `${dataQuality.blankStatus} records have blank implementation statuses.`
    );
  }
  if (!warnings.length) {
    warnings.push(locale === 'th' ? 'ไม่พบประเด็นคุณภาพข้อมูลสำคัญ' : 'No major data quality warnings detected.');
  }
  return warnings;
}

function shortenTrendLabel(value) {
  if (!value) return '';
  const [year, month] = String(value).split('-');
  if (!year || !month) return value;
  return `${month}/${year.slice(-2)}`;
}

function resolveLocale(value) {
  return value === 'th' ? 'th' : 'en';
}

